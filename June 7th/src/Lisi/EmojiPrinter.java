package Lisi;

public class EmojiPrinter {

	public static void main(String[] args) {
		printEmoji();
	}

	private static void printEmoji() {
		System.out.println(" +\"\"\"\"\"+");
		System.out.println("[| o o |]");
		System.out.println(" |  ^  |");
		System.out.println(" | '-' |");
		System.out.println(" +-----+");
	}
}
